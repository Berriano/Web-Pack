// ------------------------------- Numbers ------------------------------- //
// ------------------------- Remainder Operator -------------------------- //


'use strict';




/// % returns the remainder of a division




const leftoverOfFiveDivideTwo = 5 % 2;


console.log(leftoverOfFiveDivideTwo);


// 1   ( 5/2 = 2 with 1 leftover)





const leftoverTenDivideFour = 10 % 4;


console.log(leftoverTenDivideFour);


// 2  ( 10/4 = 2 with 2 leftover)





// -------------- function to check if number is even -------------- //



const evenNum = num => console.log(num % 2 === 0);



evenNum(2);

// true


evenNum(3);

// false


evenNum(4);

// true


evenNum(5);

// false



